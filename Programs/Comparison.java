class Comparison 
{
	public static void main (String []args)
	{
		int a = 10;
		int b = 20;
		int c = 5;
		int d = 2;
		System.out.println("Equal Result : "+(a==b));
		System.out.println("Unequal Result : "+(b!=a));
		System.out.println("Greater Than Result : "+(c>d));
		System.out.println("Lesser Than Result : "+(a<d));
		System.out.println("Greater Than Or Equal To Result : "+(a>=d));
		System.out.println("Lesser Than Or Equal To Result : "+(b<=c));
	}
}